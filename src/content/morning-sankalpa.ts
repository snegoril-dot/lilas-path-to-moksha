// Пул мягких утренних вопросов для «Утренней Санкальпы».
// Один вопрос в день, детерминированно от даты.

export const MORNING_QUESTIONS: string[] = [
  "Что для тебя важно сегодня — не по списку дел, а по сути?",
  "Какое качество ты хочешь принести в этот день?",
  "О чём сегодня стоит помнить, даже если всё пойдёт не по плану?",
  "Что просит твоего внимания прямо сейчас?",
  "Какой шаг сегодня будет честным по отношению к себе?",
  "Что ты хочешь отпустить, начиная этот день?",
  "На что откликается тело этим утром?",
  "Кому или чему сегодня можно сказать спокойное «да»?",
  "Кому или чему сегодня можно сказать спокойное «нет»?",
  "Что было бы для тебя достаточно сегодня?",
  "Какая маленькая практика вернёт тебя к себе в течение дня?",
  "Где сегодня можно быть чуть мягче — к себе или к другому?",
  "Что ты замечаешь, если сделать один вдох и не спешить отвечать?",
  "Чего ты ждёшь от этого дня — и от чего это ожидание?",
];

export function pickTodayMorningQuestion(now = new Date()): { index: number; text: string; dateKey: string } {
  const y = now.getFullYear();
  const m = String(now.getMonth() + 1).padStart(2, "0");
  const d = String(now.getDate()).padStart(2, "0");
  const dateKey = `${y}-${m}-${d}`;
  // Детерминированный индекс по дате
  const seed = Number(`${y}${m}${d}`);
  const index = seed % MORNING_QUESTIONS.length;
  return { index, text: MORNING_QUESTIONS[index], dateKey };
}
