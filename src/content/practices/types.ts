/**
 * Модель «Практики клетки» — опциональное задание на прожить,
 * длящееся от часа до недели. Между шагами — реальная жизнь.
 *
 * Все тексты — тёплые, на «ты», без медицинских, юридических
 * и финансовых советов. Никаких «должен», «обязательно» и стриков.
 *
 * Схема расширена до «коммерческого» уровня — все новые поля
 * опциональны, чтобы старые практики продолжали работать.
 */

export type PracticeDuration = "1h" | "1d" | "3d" | "7d";

export const DURATION_LABEL: Record<PracticeDuration, string> = {
  "1h": "1 час",
  "1d": "1 день",
  "3d": "3 дня",
  "7d": "7 дней",
};

/** Сколько миллисекунд длится практика выбранной длительности. */
export const DURATION_MS: Record<PracticeDuration, number> = {
  "1h": 60 * 60 * 1000,
  "1d": 24 * 60 * 60 * 1000,
  "3d": 3 * 24 * 60 * 60 * 1000,
  "7d": 7 * 24 * 60 * 60 * 1000,
};

export interface PracticeStep {
  /** Короткий заголовок шага — глагол в мягком тоне. */
  title: string;
  /** 1–2 фразы: что именно замечаем в жизни. */
  hint?: string;
}

export interface Practice {
  /** Стабильный id внутри клетки. */
  id: string;
  cellId: number;
  /** Короткое название — до 40 символов. */
  title: string;
  /** 1–2 строки: что именно фиксируем в этой практике. */
  intention: string;
  /**
   * 2–3 предложения мягкого введения — «зачем это и как относиться».
   * Отображается на экране запуска над выбором длительности.
   */
  invitation?: string;
  /** Возможные длительности — первая считается рекомендованной. */
  durations: PracticeDuration[];
  /** Рекомендованная — если хочется явно подсветить одну. */
  recommendedDuration?: PracticeDuration;
  /** 2–5 микродействий в реальной жизни. */
  steps: PracticeStep[];
  /**
   * 3–5 вопросов «поймать в течение дня» — короткие,
   * чтобы можно было задать себе на ходу.
   */
  noticePrompts?: string[];
  /** 3–5 вопросов, которые задаём при возвращении. */
  reflectionPrompts: string[];
  /** 2–3 вечерних вопроса для записи в дневник практики. */
  journalPrompts?: string[];
  /** Как понять, что практика прожита — 1–2 фразы. */
  completionCriteria?: string;
  /**
   * Когда стоит мягко прервать практику. Без медицинских советов —
   * только приглашение остановиться и позаботиться о себе.
   */
  safety?: string;
  /** Короткий текст-практика «закрытия» на экране возвращения. */
  closingRitual: string;
  /** Одна фраза-вопрос перед возвратом в игру. */
  closingReflection?: string;
  /**
   * Короткая цитата-«маяк» — строка мудрости, отражающая суть клетки.
   * Отображается на экране запуска и возвращения. Только для клеток-маяков.
   */
  quote?: {
    text: string;
    /** Источник — трактат, учитель или «народная мудрость». Без ссылок. */
    source?: string;
  };
  /**
   * Кармический переход клетки — одной строкой: откуда и куда движет карма
   * в этом плане. Помогает игроку встроить практику в путь целиком.
   */
  karmicTransition?: string;
}
