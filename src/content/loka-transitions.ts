/**
 * Возвращает текст пересечения плана, если движение из `from` в `to`
 * пересекает границу лок. Иначе — null.
 */
export function narrateLokaTransition(from: number, to: number): string | null {
  if (from < 1 || to < 1 || from > 68 || to > 68) return null;
  const a = getLoka(from);
  const b = getLoka(to);
  if (!a || !b || a.id === b.id) return null;
  // Показываем только при подъёме — при змее (спуск в нижний план) не мешаем.
  if (b.id < a.id) return null;
  const c = CROSSINGS[b.id];
  if (!c) return null;
  return `🕉 ${b.name}. ${c.description}\n\n${c.anchor}\n\n❓ ${c.question}`;
}
