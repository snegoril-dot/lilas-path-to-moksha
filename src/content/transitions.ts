/**
 * Известные циклы: пары клеток, между которыми легко застрять.
 * Ключ — клетка входа в цикл, к которой ты возвращаешься.
 */
const CYCLES: Record<number, { partner: number; text: string }> = {
  8: {
    partner: 12,
    text:
      "Цикл 8 ↔ 12: чувственное притяжение и ревность подпитывают друг друга. Что во мне сейчас хочет владеть? И согласен(-на) ли я разжать пальцы?",
  },
  12: {
    partner: 8,
    text:
      "Цикл 12 ↔ 8: ревность возвращает к чувственному плану. Где я сравниваю — и что именно во мне сейчас несвободно?",
  },
};

export function getSnakeTransition(fromId: number): TransitionText | null {
  return SNAKE_TRANSITIONS[fromId] ?? null;
}

export function getLadderTransition(fromId: number): TransitionText | null {
  return LADDER_TRANSITIONS[fromId] ?? null;
}

/** Если клетка входит в известный цикл и это уже повтор — вернуть подсказку. */
export function getCycleHint(fromId: number, visitCount: number): string | null {
  if (visitCount < 2) return null;
  return CYCLES[fromId]?.text ?? null;
}
