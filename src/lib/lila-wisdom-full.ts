// ------------------------------------------------------------------
// Иконография таттв (по планам сознания)
// ------------------------------------------------------------------
//
// Классические 5 таттв (Притхви, Апас, Тэджас, Ваю, Акаша) распределены по
// первым пяти планам Лилы. Высшие планы (тонкие и причинные) обозначены
// символами антахкараны и Абсолюта.

export interface TattvaSymbol {
  /** Короткий глиф/эмодзи для отображения рядом с номером клетки. */
  glyph: string;
  /** Полное название элемента/принципа. */
  name: string;
  /** Краткий смысл — для тултипа. */
  hint: string;
}

const PLANE_TATTVAS: Record<number, TattvaSymbol> = {
  0: { glyph: "⬛", name: "Притхви · Земля", hint: "Плотность, опора, инерция тела" },
  1: { glyph: "🌙", name: "Апас · Вода", hint: "Текучесть, очищение, эмоции" },
  2: { glyph: "🔺", name: "Тэджас · Огонь", hint: "Преображение, воля, различение" },
  3: { glyph: "⚪", name: "Ваю · Воздух", hint: "Движение, прана, связь" },
  4: { glyph: "🥚", name: "Акаша · Эфир", hint: "Пространство, звук, тонкое поле" },
  5: { glyph: "🕉", name: "Манас · Ум", hint: "Внимание, намерение, мантра" },
  6: { glyph: "☸", name: "Буддхи · Различение", hint: "Вивека, дхарма, мудрость" },
  7: { glyph: "✦", name: "Читта · Сознание", hint: "Чистое присутствие" },
  8: { glyph: "🔱", name: "Брахман · Абсолют", hint: "За пределами всех планов" },
};

export function getTattvaForCell(id: number): TattvaSymbol {
  const plane = Math.floor((id - 1) / 8);
  return PLANE_TATTVAS[plane] ?? PLANE_TATTVAS[0];
}
