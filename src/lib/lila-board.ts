const DEFAULT_WISDOM = "Каждый шаг — урок. Каждая клетка — состояние сознания.";

export const BOARD: Cell[] = Array.from({ length: 72 }, (_, i) => {
  const id = i + 1;
  let type: CellType = "normal";
  let jumpTo: number | undefined;
  if (id === 1) type = "start";
  else if (id === 68) type = "end";
  else if (SNAKES[id] !== undefined) {
    type = "snake";
    jumpTo = SNAKES[id];
  } else if (LADDERS[id] !== undefined) {
    type = "ladder";
    jumpTo = LADDERS[id];
  }
  return {
    id,
    name: NAMES[id] ?? `Клетка ${id}`,
    type,
    wisdom: WISDOMS[id] ?? DEFAULT_WISDOM,
    jumpTo,
  };
});

// Точный бросок: если выпало больше, чем до 68 — отскок
export function computeNewPosition(current: number, roll: number): number {
  const target = current + roll;
  if (target <= 68) return target;
  const overshoot = target - 68;
  return 68 - overshoot;
}

export function resolveJump(pos: number): { final: number; jumped: boolean } {
  const cell = BOARD[pos - 1];
  if (cell && cell.jumpTo) return { final: cell.jumpTo, jumped: true };
  return { final: pos, jumped: false };
}
