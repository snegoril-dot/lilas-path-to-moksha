export function getLoka(pos: number): Loka | null {
  if (pos < 1 || pos > 68) return null;
  return LOKAS.find((l) => pos >= l.range[0] && pos <= l.range[1]) ?? null;
}

/**
 * Классическое правило Джохари: для победы нужно попасть на клетку 68 ровно.
 * Если выпавшее число уводит за 68 — игрок остаётся на месте (карма ещё не готова).
 * Клетки 69-72 в классической игре недоступны обычным броском.
 */
export function computeNewPosition(current: number, roll: number): number {
  const target = current + roll;
  if (target > 68) return current; // overshoot — стоим на месте
  return target;
}

export function resolveJump(pos: number): { final: number; jumped: boolean } {
  const cell = BOARD[pos - 1];
  if (cell && cell.jumpTo) return { final: cell.jumpTo, jumped: true };
  return { final: pos, jumped: false };
}

/**
 * Классическое правило шестёрки (Хариш Джохари):
 * - Бросок 6 даёт дополнительный ход.
 * - Три шестёрки подряд — карма «перегорает»: последний бросок аннулируется
 *   (ход не применяется), счётчик сбрасывается, право хода переходит к следующему.
 *   В одиночной игре это означает: ход просто заканчивается без движения.
 */
export interface SixRuleResult {
  /** Применять ли движение по этому броску. false = бросок аннулирован. */
  applyMove: boolean;
  /** Получает ли игрок дополнительный ход после этого броска. */
  extraTurn: boolean;
  /** Новое значение счётчика подряд выпавших шестёрок (0..2; на 3 сбрасывается). */
  nextConsecutiveSixes: number;
  /** Сработало ли правило «три шестёрки подряд». */
  forfeited: boolean;
}

export function applySixRule(consecutiveSixes: number, roll: number): SixRuleResult {
  if (roll !== 6) {
    return { applyMove: true, extraTurn: false, nextConsecutiveSixes: 0, forfeited: false };
  }
  // Это третья шестёрка подряд — бросок «сгорает».
  if (consecutiveSixes >= 2) {
    return { applyMove: false, extraTurn: false, nextConsecutiveSixes: 0, forfeited: true };
  }
  return { applyMove: true, extraTurn: true, nextConsecutiveSixes: consecutiveSixes + 1, forfeited: false };
}

/**
 * Клетки с условием выхода.
 *
 * По классическим правилам Хариша Джохари специального «выхода только по N»
 * с клетки 8 нет — игрок ходит обычным броском. Цикл 8 → 12 (змея) → 8
 * прерывается сам, как только выпадет число, отличное от 4.
 * Оставляем словарь пустым, чтобы UI не показывал ложных условий.
 */
export const EXIT_CONSTRAINTS: Record<number, number[]> = {};

/** Может ли игрок покинуть текущую клетку с данным броском. */
export function canExitCell(pos: number, roll: number): boolean {
  const allowed = EXIT_CONSTRAINTS[pos];
  if (!allowed) return true;
  return allowed.includes(roll);
}

/** Человекочитаемая подсказка для клеток с условием выхода. */
export function exitHint(pos: number): string | null {
  const allowed = EXIT_CONSTRAINTS[pos];
  if (!allowed || allowed.length === 0) return null;
  if (allowed.length === 1) return `Выход из этой клетки — только при 🎲 ${allowed[0]}.`;
  return `Выход из этой клетки — при 🎲 ${allowed.join(" или ")}.`;
}
