const DEFAULT_WISDOM = "Каждый шаг — урок. Каждая клетка — состояние сознания.";

export const BOARD: Cell[] = Array.from({ length: 72 }, (_, i) => {
  const id = i + 1;
  let type: CellType = "normal";
  let jumpTo: number | undefined;
  if (id === 1) type = "start";
  else if (id === 68) type = "end";
  else if (SNAKES[id] !== undefined) {
    type = "snake";
    jumpTo = SNAKES[id];
  } else if (LADDERS[id] !== undefined) {
    type = "ladder";
    jumpTo = LADDERS[id];
  }
  // plane 0 (низший земной) внизу — клетки 1..8, plane 8 (наивысший) — 65..72
  const plane = Math.floor((id - 1) / 8);
  return {
    id,
    name: NAMES[id] ?? `Клетка ${id}`,
    type,
    wisdom: WISDOMS[id] ?? DEFAULT_WISDOM,
    jumpTo,
    plane,
  };
});

