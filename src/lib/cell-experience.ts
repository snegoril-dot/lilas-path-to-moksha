/** Take first sentence up to ~140 chars for a compact short meaning. */
function firstSentence(text: string, max = 140): string {
  const clean = text.replace(/\s+/g, " ").trim();
  const m = clean.match(/^[^.!?…]+[.!?…]?/);
  const s = (m ? m[0] : clean).trim();
  return s.length > max ? s.slice(0, max).trimEnd() + "…" : s;
}

function fallbackTone(cell: Cell): CellTone {
  switch (cell.type) {
    case "snake":
      return "shadow";
    case "ladder":
      return "gift";
    case "start":
      return "transition";
    case "end":
      return "liberation";
    default:
      return "neutral";
  }
}

export function getCellExperience(id: number): CellExperience | null {
  const cell = BOARD[id - 1];
  if (!cell) return null;
  const loka = getLoka(id);
  return {
    cell,
    lokaName: loka?.name ?? null,
    shortMeaning: SHORT[id] ?? firstSentence(cell.wisdom),
    reflectionQuestion: QUESTION[id] ?? DEFAULT_QUESTION,
    dailyPractice: PRACTICE[id] ?? DEFAULT_PRACTICE,
    tone: TONE_OVERRIDE[id] ?? fallbackTone(cell),
    keywords: KEYWORDS[id] ?? [],
  };
}

